function search() {
    let item = document.getElementById("timKiem").value;
    let sum = "";
    let product = [];
    // tạo danh sách kết quả tìm kiếm
    for (let i = 0; i < productList.length; i++) {
        if (productList[i].name === item) {
            product.push(productList[i]);
        }
    }
    //sắp xếp mảng
    product = product.sort();
    // loại các phần tử trùng nhau
    let newList = [product[0]];
    for (let i = 1; i < product.length; i++) {
        if (product[i].name !== product[i - 1].name) {
            newList.push[product[i]];
        }
    }
    //hiện lên browser
    for (let i = 0; i < newList.length; i++) {
        let name = newList[i].name;
        let price = newList[i].price;
        let url = newList[i].url;
        let HTMLcode = `
                     <div class="searchResult">
                             <img src=${url}>
                             <p class="name">${name}</p>
                             <p class="price">${price}</p>
                         </div>`;
        sum += HTMLcode;
        let a = document.getElementById("search");
        a.innerHTML = sum;
    }
}

